// Runs after document has loaded
window.addEventListener('DOMContentLoaded', () => {

    const menuToggle = document.getElementById('menu-toggle');
    const mainNav = document.getElementById('main-nav');

    menuToggle.addEventListener('click', function() {
      mainNav.classList.toggle('hidden');
    });

    // Method to hide nav on small screens
    function toggleElement() {
        if (window.innerWidth < 640) { // hide on screen size sm
            mainNav.classList.add('hidden');
            menuToggle.classList.remove('hidden');
        } else {
            menuToggle.classList.add('hidden');
            mainNav.classList.remove('hidden');
        }
    }
    window.addEventListener('resize', toggleElement);
    toggleElement(); // call initially to set the visibility based on screen size
});
